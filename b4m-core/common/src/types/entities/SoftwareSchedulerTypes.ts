/**
 * Calculate stack ranking value for a task
 *
 * Stack Ranking Schema:
 * - Core 3R: Reach (x2), Retention (x3), Revenue (x5)
 * - Extended: Romance (x1.5), Confidence (x1.5), Foundation (x2), Visibility (x1.5)
 *
 * Foundation vs Visibility creates a useful 2x2:
 * - High Foundation + High Visibility = Major feature that establishes platform
 * - High Foundation + Low Visibility = Infrastructure investment
 * - Low Foundation + High Visibility = UI polish, quick wins, delighters
 * - Low Foundation + Low Visibility = Bug fixes, maintenance, cleanup
 */
export function calculateTaskValue(task: SoftwareTask, estimate?: SWAGEstimate): number {
  // Use provided values or inferred values or defaults
  const reach = task.reach ?? estimate?.inferredStackRanking?.reach ?? 5;
  const retention = task.retention ?? estimate?.inferredStackRanking?.retention ?? 5;
  const revenue = task.revenue ?? estimate?.inferredStackRanking?.revenue ?? 5;
  const romance = task.romance ?? estimate?.inferredStackRanking?.romance ?? 5;
  const confidence = task.confidence ?? estimate?.inferredStackRanking?.confidence ?? 5;
  const foundation = task.foundation ?? estimate?.inferredStackRanking?.foundation ?? 5;
  const visibility = task.visibility ?? estimate?.inferredStackRanking?.visibility ?? 5;

  // Core 3R (business impact)
  const coreScore = reach * 2.0 + retention * 3.0 + revenue * 5.0;

  // Extended factors (team + estimation + strategic positioning)
  const extendedScore = romance * 1.5 + confidence * 1.5 + foundation * 2.0 + visibility * 1.5;

  return coreScore + extendedScore;
}

// Resource Allocation Types

/**
 * Resource allocation project (multi-project planning)
 * Inspired by an established planner pattern
 */
export interface ResourceProject {
  id: string;
  name: string;
  color: string; // For visual identification

  // Timeline (0-11 for Jan-Dec, or week numbers)
  startPeriod: number;
  endPeriod: number;

  // Phases within the project
  phases: ResourcePhase[];

  // Metadata
  priority: number; // Display order (lower = higher priority)
  status: 'active' | 'planned' | 'completed' | 'on_hold';
}

/**
 * Phase within a project (QA, Development, Maintenance, Launch, etc.)
 */
export interface ResourcePhase {
  id: string;
  name: string;
  color: string; // Phase-specific color

  // Percentage of project duration (0-100)
  pct: number;

  // Team assignments: resource ID -> allocation percentage (0-100)
  teamWeights: Record<string, number>;
}

/**
 * Resource (team member) for allocation planning
 */
export interface Resource {
  id: string;
  name: string;
  role?: string;

  // Skills/tags for filtering
  skills: string[];

  // Monthly/weekly availability (period key -> percentage 0-100)
  // e.g., { "2026-01": 100, "2026-02": 80 } or { "W1": 100, "W2": 50 }
  availability: Record<string, number>;

  // Link to SoftwareEngineer if available (for velocity data)
  engineerId?: string;
}

/**
 * Computed allocation for a resource in a period
 */
export interface ResourceAllocation {
  resourceId: string;
  period: string; // "2026-01" or "W1"

  // Allocations by project
  allocations: {
    projectId: string;
    projectName: string;
    phaseId: string;
    phaseName: string;
    percentage: number;
  }[];

  // Total percentage (should be ≤100 for no conflict)
  totalPercentage: number;
}

/**
 * Resource conflict (over-allocation warning)
 */
export interface ResourceConflict {
  resourceId: string;
  resourceName: string;
  period: string;

  // What caused the conflict
  allocations: {
    projectId: string;
    projectName: string;
    percentage: number;
  }[];

  // Total allocation (>100 means conflict)
  totalPercentage: number;

  // Severity
  severity: 'warning' | 'critical'; // warning: 100-120%, critical: >120%
}

/**
 * Resource allocation plan (full planning state)
 */
export interface ResourceAllocationPlan {
  id: string;
  name: string;

  // Planning period type
  periodType: 'monthly' | 'weekly';
  startPeriod: string; // "2026-01" or "2026-W01"
  endPeriod: string;

  // Data
  projects: ResourceProject[];
  resources: Resource[];

  // Computed fields (generated from projects and resources)
  allocations?: ResourceAllocation[];
  conflicts?: ResourceConflict[];

  // Metadata
  createdAt: number;
  updatedAt: number;
}

/**
 * Calculate resource allocations from projects
 */
export function calculateResourceAllocations(
  projects: ResourceProject[],
  resources: Resource[],
  startPeriod: number,
  endPeriod: number
): ResourceAllocation[] {
  const allocations: Map<string, ResourceAllocation> = new Map();

  // Initialize allocations for each resource-period combination
  for (const resource of resources) {
    for (let period = startPeriod; period <= endPeriod; period++) {
      const key = `${resource.id}-${period}`;
      allocations.set(key, {
        resourceId: resource.id,
        period: period.toString(),
        allocations: [],
        totalPercentage: 0,
      });
    }
  }

  // Calculate allocations from projects
  for (const project of projects) {
    if (project.status !== 'active' && project.status !== 'planned') continue;

    const projectDuration = project.endPeriod - project.startPeriod + 1;

    for (const phase of project.phases) {
      // Calculate phase periods
      const phaseStartOffset =
        project.phases.slice(0, project.phases.indexOf(phase)).reduce((sum, p) => sum + p.pct, 0) / 100;
      const phaseEndOffset = phaseStartOffset + phase.pct / 100;

      const phaseStartPeriod = Math.floor(project.startPeriod + phaseStartOffset * projectDuration);
      const phaseEndPeriod = Math.min(
        Math.floor(project.startPeriod + phaseEndOffset * projectDuration),
        project.endPeriod
      );

      // Add allocations for each team member in this phase
      for (const [resourceId, percentage] of Object.entries(phase.teamWeights)) {
        if (percentage <= 0) continue;

        for (let period = phaseStartPeriod; period <= phaseEndPeriod; period++) {
          if (period < startPeriod || period > endPeriod) continue;

          const key = `${resourceId}-${period}`;
          const allocation = allocations.get(key);
          if (allocation) {
            allocation.allocations.push({
              projectId: project.id,
              projectName: project.name,
              phaseId: phase.id,
              phaseName: phase.name,
              percentage,
            });
            allocation.totalPercentage += percentage;
          }
        }
      }
    }
  }

  return Array.from(allocations.values());
}

/**
 * Detect resource conflicts from allocations
 */
export function detectResourceConflicts(allocations: ResourceAllocation[], resources: Resource[]): ResourceConflict[] {
  const resourceMap = new Map(resources.map(r => [r.id, r]));
  const conflicts: ResourceConflict[] = [];

  for (const allocation of allocations) {
    if (allocation.totalPercentage > 100) {
      const resource = resourceMap.get(allocation.resourceId);
      conflicts.push({
        resourceId: allocation.resourceId,
        resourceName: resource?.name || allocation.resourceId,
        period: allocation.period,
        allocations: allocation.allocations.map(a => ({
          projectId: a.projectId,
          projectName: a.projectName,
          percentage: a.percentage,
        })),
        totalPercentage: allocation.totalPercentage,
        severity: allocation.totalPercentage > 120 ? 'critical' : 'warning',
      });
    }
  }

  return conflicts;
}
