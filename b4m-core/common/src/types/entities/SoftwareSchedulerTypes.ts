/**
 * Software Project Scheduling Types for B4M Pi
 *
 * SWAG Engine (Scientific Wild-Ass Guess) + Gantt scheduling
 * Lifted from optimal-viz project.
 */

/**
 * A software task (GitHub issue, JIRA ticket, etc.)
 */
export interface SoftwareTask {
  // Identity
  id: string;
  title: string;
  description: string;
  url?: string;

  // Classification
  type: 'bug' | 'feature' | 'refactor' | 'task' | 'subtask' | 'epic' | 'unknown';
  /** Original issue type name from source (e.g. Jira custom types like "Spike", "Design Task") */
  issueTypeName?: string;
  /** Current workflow status (e.g. "In Progress", "To Do") - primarily from Jira */
  status?: string;
  priority: 'P0' | 'P1' | 'P2' | 'P3' | 'unknown';
  labels: string[];
  labelColors?: Record<string, string>; // label name → hex color (e.g. "d73a4a")

  // Dependencies
  predecessors: string[]; // Task IDs that must complete first
  blockedBy?: string[]; // External blockers (text descriptions)

  // Assignment
  assignee?: string; // Engineer ID if assigned
  requiredSkills?: string[];

  // Time constraints
  hardDeadline?: number; // Timestamp - client commitment
  softDeadline?: number; // Timestamp - target

  // Stack ranking vectors (0-10 scale, optional - can be inferred)
  reach?: number; // How many users affected
  retention?: number; // Does it keep users coming back
  revenue?: number; // Direct/indirect monetization
  romance?: number; // Team excitement, learning opportunity
  confidence?: number; // Estimate certainty
  foundation?: number; // 0 = edge polish, 10 = core infrastructure/platform
  visibility?: number; // 0 = invisible plumbing, 10 = hero feature users notice

  // Source tracking
  source: 'github' | 'jira' | 'csv' | 'manual';
  sourceId?: string;

  // Raw text for AI analysis
  rawText: string;
}

/**
 * SWAG (Scientific Wild-Ass Guess) estimate generated by AI
 */
export interface SWAGEstimate {
  taskId: string;

  // Duration estimate with uncertainty (in hours)
  duration: {
    p50: number; // Median case
    p80: number; // Conservative
    p95: number; // Worst realistic
  };

  // What the AI inferred
  inferredComplexity: 'trivial' | 'simple' | 'medium' | 'complex' | 'epic';
  inferredSkills: string[];
  inferredType: 'bug' | 'feature' | 'refactor' | 'integration' | 'config';

  // Similar completed work (for calibration)
  similarClosedIssues: SimilarIssue[];

  // Confidence in the SWAG (0-1)
  confidence: number;
  confidenceFactors: string[];

  // Adjustment factors
  adjustments: {
    ifAssignedTo: Record<string, number>; // Engineer ID → multiplier
    ifBlockedBy: Record<string, number>; // Blocker type → delay hours
  };

  // Explanation for humans
  reasoning: string;

  // Inferred stack ranking (if not provided)
  inferredStackRanking?: {
    reach: number; // How many users affected
    retention: number; // Does it keep users coming back
    revenue: number; // Direct/indirect monetization
    romance: number; // Team excitement, learning opportunity
    confidence: number; // Estimate certainty
    foundation: number; // 0 = edge polish, 10 = core infrastructure/platform
    visibility: number; // 0 = invisible plumbing, 10 = hero feature users notice
  };

  // Anomaly detection - flags estimates that deviate significantly from historical norms
  anomaly?: {
    deviationRatio: number; // estimate p50 / historical median (>2 or <0.5 = flagged)
    historicalMedianHours: number; // the historical median for this task type
    historicalCount: number; // sample size behind the median
    direction: 'over' | 'under'; // estimate is higher or lower than expected
    description: string; // human-readable explanation
  };
}

export interface SimilarIssue {
  issueId: string;
  title: string;
  similarity: number; // 0-1 semantic similarity
  actualDuration: number; // How long it really took (hours)
  completedBy?: string; // Who did it
}

/**
 * Engineer/resource for software projects
 */
export interface SoftwareEngineer {
  id: string;
  name: string;

  // Skills (learned from completed work)
  skills: string[];
  skillStrengths: Record<string, number>; // Skill → proficiency 0-1

  // Velocity (learned from history)
  velocity: {
    overall: number; // Issues/week average
    byType: Record<string, number>; // Type → issues/week
    byComplexity: Record<string, number>; // Complexity → multiplier
    trend: 'improving' | 'stable' | 'declining';
  };

  // Availability
  availability: {
    hoursPerWeek: number;
    currentLoad: number; // 0-100%
  };

  // Repo constraints
  allowedRepos?: string[]; // If set, engineer can only work on tasks from these repos

  // For new engineers
  isRamping: boolean;
  rampStartDate?: number;
  rampMultiplier?: number; // Current productivity multiplier (0-1)
}

/**
 * Software project scheduling problem
 */
export interface SoftwareProject {
  id: string;
  name: string;
  description: string;
  createdAt: number;
  updatedAt: number;

  // Tasks to schedule
  tasks: SoftwareTask[];

  // SWAG estimates (generated by AI)
  estimates: SWAGEstimate[];

  // Team
  engineers: SoftwareEngineer[];

  // Constraints
  constraints: SoftwareProjectConstraints;

  // Objective
  objective: SoftwareProjectObjective;

  // Solver mode
  mode: SolverMode;

  // Duration mode - which estimate percentile to use (default: p50)
  durationMode?: DurationMode;
}

export interface SoftwareProjectConstraints {
  // Time constraints
  sprintLengthDays?: number;
  planningHorizonDays?: number;

  // Capacity constraints
  maxHoursPerEngineerPerDay?: number;
  respectAvailability: boolean;

  // Skill constraints
  requireSkillMatch: boolean;

  // Hard deadlines (must satisfy)
  hardDeadlinesMustMeet: boolean;

  // If true, only schedule tasks with explicit assignees; unassigned tasks are skipped
  respectAssignments?: boolean;
}

export interface SoftwareProjectObjective {
  // Primary objective
  primary: 'minimize_makespan' | 'maximize_value' | 'minimize_risk' | 'balanced';

  // Weights for multi-objective (0-1, should sum to ~1)
  weights?: {
    makespan: number; // Lower is better
    value: number; // Higher is better (stack ranking score)
    risk: number; // Lower is better (prefer high confidence)
    morale: number; // Romance balance
  };
}

/**
 * Duration mode - which estimate percentile the solver uses for scheduling
 */
export type DurationMode = 'p50' | 'p80' | 'p95';

/**
 * Solver mode - different optimization profiles
 */
export type SolverMode =
  | 'only_bugs' // Minimize time to zero P1/P2
  | 'strategic' // Maximize synergy + revenue
  | 'quick_wins' // Max tasks in 1 week
  | 'max_fun' // Maximize romance
  | 'revenue_protection' // Retention ≥ 8 OR revenue ≥ 7
  | 'deadline_sprint' // Minimize P(miss deadline)
  | 'balanced'; // Pareto-optimal

